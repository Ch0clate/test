package com.coxandkings.travel.bookingengine.resource.gateway;

public class DateOfBirth
{
    //variables
    private long month=0L;
    private long year=0L;
    private long day=0L;
    //setter getter
    public long getMonth() {
        return month;
    }

    public void setMonth(long month) {
        this.month = month;
    }

    public long getYear() {
        return year;
    }

    public void setYear(long year) {
        this.year = year;
    }

    public long getDay() {
        return day;
    }

    public void setDay(long day) {
        this.day = day;
    }
}
