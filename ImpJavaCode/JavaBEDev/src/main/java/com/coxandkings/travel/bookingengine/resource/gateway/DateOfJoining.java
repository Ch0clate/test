package com.coxandkings.travel.bookingengine.resource.gateway;

public class DateOfJoining {
    //variables
    private Long day;
    private Long month;
    private Long year;
    //setter getter
    public Long getDay() {
        return day;
    }

    public void setDay(Long day) {
        this.day = day;
    }

    public Long getMonth() {
        return month;
    }

    public void setMonth(Long month) {
        this.month = month;
    }

    public Long getYear() {
        return year;
    }

    public void setYear(Long year) {
        this.year = year;
    }
}
